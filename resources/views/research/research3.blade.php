      <h2>Post Budget Market Reflection of Increase Tax on Smokeless Tobacco (Jorda) in Bangladesh</h2>
      <p>
        Bangladesh is a highly densely populated country and tobacco consumption is also high.
        According to Global Adult Tobacco Survey-2017, In Bangladesh 37.8 million adults
        currently used tobacco in different format. Among these 19.2 million adults used tobacco in
        smoking format (15 million used cigarettes & 5.3 million used bidi). 22 million adults (16.2%
        men and 24.8% women) currently used smokeless tobacco (GATS 2017). The report shows
        that the smokeless tobacco user is higher than smoking tobacco. But the collection of tax
        from SLT is very tinny than the tax from cigarette. In the fiscal year 2018-2019, total tax
        collection from cigarette was 27,620 Core Bangladeshi taka on the other hand the total tax
        collection from Jorda was only 47 Core taka. So, it is very clear that the huge tax evasion is
        there. The price of Jorda has been increased 10 BDT per 10 grams in the current budget
        (2020-2021), now the lowest price is taka 40, which is somewhat satisfactory. But the
        collection of tax was very poor for complex market structure and unregistered tobacco
        industry.
      </p>
      <p>
        The major objective of the study was to identify the market reflection regarding the changing
        price (As per budget). Both qualitative and quantitative approach and observational
        methods were followed to complete the study. After 30 days of announcing budget, the
        research team were randomly selected 6 (Six) districts (Dhaka, Khulna, Barisal,
        Mymenshing, Jhinaidha, and Meherpur) to complete the study. Data were collected from
        all wholesale market of each district from 5 August 2020 to 20 August 2020.
      </p>
      <p>
        Total 125 packets of Jorda were investigated (46 company 54 brands) which was collected
        from 6 (Six) districts to identify the market reflection on raising tax. The team analyzed the
        data regarding current price and previous price of passing the national budget but almost
        all the product sale with previous price. Only 56% brands and 43% packets change the price
        but the range was only 1-2 taka and the average increasing rate is 1.72 taka. However, none
        of the 125 Jorda collected are being sold at the price mentioned in the budget passed in the
        fiscal year 2020-21. Since the price of most of the Jorda in the country are very low, it is easily
        available to the consumers and even, if it increases its price, it does not become too much of
        a burden for the consumers.
      </p>

