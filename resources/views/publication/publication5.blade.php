      <h3>Revenue from Smokeless Tobacco</h3>
      <p>
        Total 486.5 million BDT revenue was received from smokeless tobacco products during the last 2017-
        2018 fiscal years. Of this, the revenue came from Zarda, was 470.1 million BDT and the revenue
        from Gul was 16.4 million BDT. If all smokeless tobacco companies are brought under proper
        taxation system, the revenue generated will be increase several hundred times. But in Bangladesh
        there are hundreds of Zarda and Gul producing companies, which are still outside the tax base.
      </p>
      <h3>Smokeless Tobacco has a Specific Market System: TCRC Report</h3>
      <p>
        Smokeless tobacco production is small and unorganized, majority of the companies are not registered
        legally. So it is difficult to find these factories. Factory owners adopt several unfair means to defraud
        the revenue and laws. These include setting up factories in the household levels, not registering
        companies and factories, selling of products to other regions then the area of production, transferring
        the factory for tax evasion, not having the proper name and address of the tobacco company, having
        multiple products in the same name, having multiple companies under same ownership, inserting false
        addresses, false VAT registration, trademark and use of fabricated BSTI logos. However, no strong
        steps has been taken, so far to bring the companies under strong regulation. The government of
