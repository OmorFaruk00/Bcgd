      <h2>Background</h2>
      <p>
        n Bangladesh 37.8 million adults currently used tobacco in
        different format. Among these 19.2 million adults used tobacco in
        smoking format (15 million used cigarettes & 5.3 million used bidi). 22
        million adults (16.2% men and 24.8% women) currently used smokeless
        tobacco (GATS 2017). The report shows that the smokeless tobacco user
        is higher than smoking tobacco. As per Bangladesh Cancer Society total
        12.6 million people died for using tobacco in Bangladesh, which makes
        total expenses 30,570 Coror Bangladeshi taka in a year.
      </p>
      <h2>Objectives</h2>
      <p>
        The primary objectives of the study was to identify the
        distribution policy of Smokeless and bidi and the major objectives were
        to measure the obstacles for implementation of Pictorial warnings and
        find out the best/suitable packets for effective implementation of GHWs
        for Smokeless and Bidi.

      </p>
      <h2>Methodology</h2>
      <p>
        Cross sectional study design, quantitative and qualitative
        approaches have been used to conduct the study. In qualitative
        approach observational methods and checklist form were developed to
        collect the primary data. Checklist form was developed on the basis of
        section 10 of tobacco control law in Bangladesh. All types of tobacco
        products (all Varity and unique pack as per availability) was observed for
        GHW implementation as per law (all contents of section 10) and three
        retail shops and three wholesale shops was selected in a big market
        area (Mostly Public Gathering Place) from a district. Purposive sampling
        methods and systematic technique was followed. Data were collected
        by using GHWs monitoring software from all 64 districts by 8 cycles in 8
        different times. 8 Districts and sub-districts were selected covering from
        all 8 divisions in a cycle. Data collection period were selected on the
        basis of tobacco control law. As per law tobacco industry must change
        rotationally the image of GHWs (In every three months). Thatâ€™s why data
        collection period was on the three months. All types of tobacco
        products (as per availability and unique pack) were observed for GHW
        implementation as per law and at least three retail shops and three
        wholesale shops were selected in a big market area from each district.
      </p>
      <h2>Findings and Results</h2>
      <p>
        Total 10,641 packets were collected and analyzed that covered all 64
        districts and sub-districts which include 270 big market areas and 583
        wholesale shops. The study were found 10, 12 and 20 stick size paper
        packet using for cigarette manufacturing and 20 and 25 sticks filter and
        non-filter normal paper round shape packets used for bidi packaging.
        GHWs implementation and monitoring is very complex on
        Jordha/Zordha because different size, shapes, structures and ingredients
        of the packet. The study were found 42% Tin Casket/Box, 34% Plastic
        Casket/Box, 16% poly pack and 8% others forms used for Jordha/Zordha
        packaging. On the other hand GHWs implementation is very challenge
        on Gul pack because of small round shape plastic box used for
        packaging.
      </p>
      <p>
        Besides Tobacco Industry using attractive clour, Mosque theme design,
        Halal, Arabic Letter, Indian packets design with hindi letter, religious
        brand name, logo and clour (Muslim and Hindu) for draw customer
        attention. SLT products especially Jordha/Zordha using different brand
        element (Sade Gondhe Atullonio, best quality, Sade alada etc) and
        adding different flavor for creating positive mindset (Not Harmful)
        among the users.
      </p>
      <p>
        In the last cycle study the visibility of GHWs on Bidi was 85% , Zordha
        87% and Gul 88% but only 3% Zordha pack 100% compliance and no
        other pack was compliance 100 %. GHWs were not clear and
        understandable among 73% bidi pack, 82% violate to print both side and
        91% packets covered GHWs by Banderole/Taxrole. 65% gul pack violates
        to print GHWs on 50% principal area, 80% violate to print both side and
        95% GHWs was not clear and understandable. In zordha, 39% image of
        GHWs was used foreign picture, 94% pack violates to print both side,
        58% pack violate to print 50% principal area and 95% image was not
        clear and understandable.
      </p>


