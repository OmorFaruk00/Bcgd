        <h3 >MEMORANDUM OF UNDERSTANDING</h3>
              <p>
                This Memorandum of Understanding (MoU) is made and entered into on 18 November 2023 by
                the South Asian Network for Public Administration, hereinafter referred to as “SANPA” and the
                Bangladesh Center for Governance and Development (BCGD) of Dhaka International University,
                hereinafter referred to as “BCGD-DIU.”
              </p>
              <h4>Preamble</h4>
              <p>
                Whereas, SANPA and BCGD-DIU share the same vision and objectives to promote public
                administration and governance research and education to contribute to inclusive development
                and accountable governance in South Asia and beyond;
              </p>
            <p>
              Whereas, SANPA and BCGD-DIU aim to strengthen academic and research collaborations by
              attracting scholars and practitioners from the South Asia region and beyond and become
              platforms of academics and practitioners further to promote public administration education,
              research, and capacity building;
            </p>
            <p>
              Therefore, SANPA and BCGD-DIU agree to forge partnerships in the form of a Memorandum of
              Understanding (MoU) under the following terms and conditions.
            </p>
    </div>
    <div class="item">
        <h4>Modalities of Collaboration</h4>
          <p>
            In this context, as both institutions are united by common interests and objectives in research
            and academic collaboration in areas of public administration as an academic discipline
            and a field of practice, public policy and governance and inclusive development, and they wish
            to maintain and promote collaboration that permit those mutual scholarly and academic ties
            and promote closer collaboration.
          </p>
          <p>
            <strong>SANPA and BCGD-DIU, therefore, agree to collaborate in the following areas:</strong>
          </p>
          <p>
            <strong>1.</strong>  Collaboration on research and publications on broader areas of process and practices of
            public administration, public policy, governance dynamics, development of course curricula
            catering to the need for changing dimensions of governance demand and deficit.
          </p>
          <p>
            <strong>2.</strong>  Joint research on dynamics and process of design and implementation issues of public
            policies and public administration practices and global development agenda such as the
            Agenda 2030;
          </p>
          <p>
            <strong>3.</strong>  Develop new case studies on public sector management, policy implementation, program
            management issues on the basis of the existing and forthcoming collaborative researches;
          </p>
          <p>
            <strong>4.</strong>  Institutional collaboration in organizing regional and international seminar, conference
            and workshop.
          </p>
          <p>
            <strong>5.</strong>  Jointly organizing workshops/ seminars and international conferences with any other
            national or international public or private institution/organisation;
          </p>
          <p>
            <strong>6.</strong> This agreement will govern the general framework for joint research, publications,
            arrangement of workshops, seminars, and conferences, and any other mutually agreed
            activities. Individual working agreement(s) for such collaboration, should they arise, will be
            executed for specific projects/ activities under this agreement.
          </p>
          <p>
            <strong>7.</strong>  The individual working agreement(s) referred to in paragraph 2 may be developed for, but
            need not be limited to, joint research and publications, arranging workshops, seminars,
            conferences, etc.
          </p>
          <p>
            <strong>8.</strong>  The cost of any joint activity shall be mutually determined.
          </p>
          <p>
            <strong>9.</strong> This Memorandum of Understanding (MoU) may be modified or extended by written consent
            of both parties.
          </p>
          <p>
            <strong>10.</strong>  Either Party may terminate this MOU by giving three months’ prior notice in writing to the
            other party
          </p>
          <p>
            <strong>11.</strong>  This Memorandum of Understanding (MoU) will become effective on the date above and
            remain for five years from that date. It may be renewed for an additional period by an exchange
            of letters between the parties.
          </p>
          
          <div class="mt-4">
            <h4>PROTECTION OF INTELLECTUAL PROPERTY RIGHTS</h4>
            <p>
              <strong>1.</strong>  The protection of intellectual property rights shall be enforced in conformity with the
              national laws, rules and regulations signed by both Parties.
            </p>
            <p>
              <strong>2.</strong>  In the event that either Party wishes to use the name, logo and/or official emblem of the
              other Party for any purpose shall obtain the written consent of the other Party in advance.
            </p>
            <p>
              <strong>3.</strong>  Each of the Parties shall not publish any marketing material relating to this MOU without the
              prior written consent of the other Party and undertake that the wording for any marketing
              materials shall be agreed jointly between the Parties prior to its use.
            </p>
          </div>

          <div class="mt-4">
            <h4>CONFIDENTIALITY</h4>
            <p>
              <strong>1.</strong>  Each party shall observe and maintain the confidentiality and secrecy of documents,
              information and other data received from or supplied to the other Party during the period of
              the effectivity of this Memorandum of Understanding or any other agreements made pursuant
              to this Memorandum of Understanding unless such information is required to be disclosed by
              law or any legal or regulatory authority.
            </p>
            <p>
              <strong>2.</strong>  For purposes of paragraph 1 above, such document, information and data include any
              document, information and data which is disclosed by a Party (the Disclosing party) to the other
              Party (the Receiving party), prior to, or after, the execution of this Memorandum of
              Understanding, involving technical, business, marketing, policy, know-how, planning, project
              management and another document, information, data and/or solutions in any form, including
              but not limited to any document, information or data which is designated in writing to be
              confidential or by its nature intended to be for the knowledge of the Receiving Party or if orally
              given, is given in the circumstances of confidence.
            </p>
            <p>
              <strong>3.</strong>  Both Parties agree that the provisions of this Article shall continue to be binding between the
              Parties notwithstanding the termination of this Memorandum of Understanding.
            </p>
            <h4 class="mt-4">SUSPENSION</h4>
            <p>
              Each Party reserves the right for reasons of national security, national interest, public order or
              public health to suspend, either in whole or in part, the implementation of this Memorandum of
              Understanding, which suspension shall take effect immediately after notice given to the other
              Party through mutual consultation.
            </p>
            <h4 class="mt-4">SETTLEMENT OF DISPUTES</h4>
            <p>
              Any difference or dispute between the Parties concerning the interpretation and/or
              implementation and/or application of any of the provisions of this Memorandum of
              Understanding shall be settled amicably through mutual discussion and/or negotiations or
              arbitration between the Parties.
            </p>

