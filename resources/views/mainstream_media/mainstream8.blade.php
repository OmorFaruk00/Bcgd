    <div class="item">
      <p>
        Speakers at a discussion have said that tobacco companies are applying various tactics overuse of electronic cigarettes, which is known as e-cigarette. The companies are promoting e-cigarette and encouraging youths in various ways, which poses a serious threat to the young generation. Against this backdrop, it is important to impose ban on e-cigarette in a bid to protect the young generation of Bangladesh. It must be done without any delay, they added.
      </p>
      <p>
        The speakers called upon the authorities concerned to take immediate steps in this regard. They also called for taking steps to amend the existing tobacco control law. Besides, the opportunity of importing e-cigarette under the existing finance law must be stopped, they said.
      </p>
      <p>
        Tobacco Control and Research Cell (TCRC) of Dhaka International University arranged the discussion on “e-cigarette is a New Threat for the Youth: Ways to Stop Its Use’ at the auditorium of the Officers’ Club in Dhaka on Tuesday morning (November 3, 2020).
      </p>
      <p>
        Deputy Speaker of the National Parliament of Bangladesh Advocate Fazle Rabbi Miah, MP, spoke at the event as the chief guest with Barrister Shameem Haider Patwary, MP, in the chair. Chairman of Parliamentary Standing Committee on the Home Ministry Advocate Md Shamsul Haque Tuku, MP, also spoke at the event, while Fazle Hossain Badsha, MP, Hasanul Haq Inu, MP, Rana Mohammad Sohel, MP, Dr. Habibe Millat, MP, Shirin Akhter, MP, and Prof Dr. Masuda M Rashid Chowdhury, MP, joined the discussion via Zoom. National Professor Brig (Retd) Dr. Abdul Malik, President of National Anti-Tuberculosis Association of Bangladesh (NATAB) Muzaffar Hossain Paltu, Prof Dr. Pran Gopal Datta, Dr. Mahfuzul Haq, National Professional Officer, WHO; Gan Quan, Director of the international organization The Union, Dr. Amit Yadav, Senior Technical Advisor of The Union, Ms. Vandana Shaha, Director (Tobacco Control & NCD), Campaign for Tobacco-Free Kids (South Asian Region) also joined the discussion through online.
      </p>
      <p>
        Advocate Syed Mahbubul Alam presented the keynote paper at the event. In his keynote paper, he said that e-cigarette is being imported and sold everywhere in Bangladesh at very cheap prices in absence of clear law. Cigarette companies are encouraging youths to e-cogenerate, saying that it is less harmful. However, e-cigarette has already been banned in 42 countries, including India, to protect people from its harmful impact. A total of 56 countries have imposed restrictions on the sale and purchase of e-cigarette, he mentioned.
      </p>
      <p>
        In his speech, Advocate Fazle Rabbi Miah said, “We will draw the attention of Prime Minister to stop e-cigarette in the country. We should create a Caucus to control tobacco use. We believe that we will implement the tobacco-free Bangladesh by 2040 as per the declaration of the Prime Minister.”
      </p>
      <p>
        Barrister Shameem Haider Patwary said that tobacco companies are violating the tobacco control law as they are publishing tobacco advertisements on YouTube, Facebook, websites, and other social networks. However, they are encouraging those people, who want to quit smoking, to use e-cigarette instead of conventional ones. It is now necessary to formulate strict law and strict enforcement of the existing law to stop the spread of e-cigarette, he opined.
      </p>
      <p>
        Terming that smoking is the gateway to drugs, Advocate Shamsul Haque Tuku said, “We must prevent our new generation from harmful product-- e-cigarette. It is necessary to take immediate initiative to stop the import of e-cigarette. If requires, a special ordinance must be promulgated in this regard.”
      </p>
      <p>
        In his speech, Hasanul Haq Inu categorically said that e-cigarette inspires the youth to use tobacco products. There is no reason to think that e-cigarette is safer than cigarette.
      </p>
      <p>
        Fazle Hossain Badsha criticized the tobacco companies over the issue. “e-cigarette traders have put out future generations at risk for profit. We, the Members of Parliament, need to enact strict law to protect the youth from the harmful impacts of e-cigarette,” he said.
      </p>
      <p>
        Coming down heavily on tobacco companies, Dr. Habibe Millat said, “Cigarette companies are killing our future generations. We, all the MPs, agree on stopping e-cigarette. Government and non-government platforms should work together to stop the spread of e-cigarette in the country,” he said.
      </p>
      <p>
        Echoing the same as other speakers, Shirin Akhter said that before the spread of e-cigarette, its import should be completely banned.
      </p>
      <p>
        Prof Dr. Masuda M Rashid Chowdhury said, e-cigarette has already been banned in various countries, considering the health risks. There is no alternative to banning it in Bangladesh in the interest of protecting the new generation, she said.
      </p>
      <p>
        Over hundreds of anti-tobacco activists from home and abroad also joined the discussion through Zoom software.
      </p>
