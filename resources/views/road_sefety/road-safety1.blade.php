                <h2 class="title-section">Key Discussions</h2>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> Speakers at the roundtable emphasized the importance of a comprehensive road safety law in Bangladesh to achieve Sustainable Development Goals (SDGs) and UN's Global Plans to reduce casualties from road accidents.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> The National Road Safety Day-2023, with the theme "Ain Meney Sorokey Choli, Smart Bangladesh Gori" will be observed on October 22, 2023.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> Experts emphasized the importance of a separate law for road safety based on the internationally recognized five pillars or safe systems approach. These five pillars include safe road users, safe vehicles, safe speeds, safe roads, and post-crash care.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> Aminul Islam Sujon, technical adviser-road safety at Vital Strategies, highlighted that the existing laws, particularly the Road Transport Act of 2018, primarily focus on transport issues but do not adequately address road safety concerns.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> The Road Transport Act of 2018 lacks coverage of crucial road safety issues, such as road infrastructure, vehicle safety, post-crash rescue and treatment, helmet usage, and child safety.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> Ilias Kanchan, chairman of Nirapad Sarak Chai, echoed the need for a separate road safety law, emphasizing that the existing laws have limitations in ensuring road safety.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> Road crashes were identified as one of the leading causes of deaths and injuries in Bangladesh, necessitating significant efforts to reduce these road mishaps.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> The lack of dedicated legislation addressing road safety measures in the country was acknowledged, emphasizing the need for strong policy and necessary legal frameworks.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> According to the Bangladesh Road Transport Authority (BRTA), 3727 people were killed and 5781 injured in road crashes in the country from January to September.
          </p>
          <p style="text-align:justify">
            <i class="fas fa-square"></i> The comprehensive road safety law must include evidence-based road safety measures and reflect global best practices, such as multi-modal transportation, safe road infrastructure, post-crash response, safe road use, and addressing key behavioral risk factors.
          </p>

          <h2 class="title-section">Conclusion</h2>
          <p  style="text-align:justify">
            The roundtable meeting shed light on the urgent need for a comprehensive road safety law in Bangladesh. The speakers emphasized the importance of a legal framework that incorporates the Safe System Approach and addresses various aspects of road safety. It is evident that ensuring road safety is a collective responsibility, and the enactment of a comprehensive road safety law will provide the legal basis for all relevant government departments and ministries to take effective actions to reduce road accidents and casualties in the country.
          </p>
          <p style="text-align:justify">
            This report highlights the consensus among experts and stakeholders that the current legislation is insufficient to address the complex challenges of road safety, and a dedicated legal framework is crucial for achieving safer roads in Bangladesh.
          </p>
