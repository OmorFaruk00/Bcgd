      <p>
        Recently, the drafting amendment of the Smoking and Tobacco Products Usage (Control) Act-2005 (amendment 2013)
        has included a provision banning the production and marketing of E-Cigarettes, which is truly commendable.
        Health experts’ opinion is E-Cigarettes are more harmful than conventional cigarettes.
      </p>
      <p>
        Emphasizing the issue a discussion meeting on the "Importance of Banning E-Cigarettes in Bangladesh" was held at
        Seminar Room, WBB Trust, Dhanmondi at 11 am on January 30, 2023. Tobacco Control and Research Cell (TCRC), Dhaka
        International University (DIU), and Bangladesh Anti-Tobacco Alliance (BATA) jointly organized the event.
      </p>
      <p>
        Additional Deputy Inspector General of Special Branch of Bangladesh Police Ms. Rockfar Sultana Khanam, Head of
        Programs-Bangladesh of Vital Strategies Mr. Md. Shafiqul Islam, Director of Health Sector of Dhaka Ahsania
        Mission Mr. Iqbal Masud, Secretary General of Pratyasha Anti-Drugs Club Mr. Helal Ahmed was as the guest of
        honor of the program and Associate Professor of Dhaka International University (DIU) Mr. Bazlur Rahman delivered
        the welcome speech. Coordinator of Bangladesh Anti-Tobacco Alliance (BATA) Mr. Saifuddin Ahmed presided over the
        program.
      </p>
      <p>
        The Union's Technical Advisor (Bangladesh) Advocate Syed Mahbubul Alam presented a keynote on the “Importance of
        Banning E-Cigarettes in Bangladesh”. Mr. Hamidul Islam Hillol, Program Manager of Bureau of Economic Research of
        Dhaka University (DU) moderates the program. Also, representatives of the Bangladesh Anti-Tobacco Alliance and
        various organizations working national and international levels on tobacco control were present in the program.
      </p>
      <p>
        Mr. Syed Mahbubul Alam said in the keynote presentation that tobacco companies are spreading myths on social
        media to make people addicted to e-cigarettes as an alternative to cigarettes. Their main target is the young
        generation to make long-term consumers. E-cigarettes should be banned right now to make tobacco-free Bangladesh
        before 2040 to implement the honorable Prime Minister’s promise.
      </p>
      <p>
        Ms. Rockfar Sultana Khanam said Emerging Tobacco Products, Electronic Nicotine Delivery System, Heat-Not-Burn or
        E-Cigarettes whatever called are very harmful to the body like other tobacco products. It’s a new strategy of
        tobacco companies to get the young generation addicted. It is necessary to take quick and effective steps to
        control E-Cigarettes with the National Tobacco Control Cell.
      </p>
      <p>
        Mr. Iqbal Masud said that E-Cigarettes are not less harmful in any way compared to conventional cigarettes in
        the market. But Companies spread falsehoods in their favor worldwide. He called upon all those working at the
        policy-making level to work together to ban this product.
      </p>
      <p>
        Mr. Md. Shafiqul Islam said the use of E-Cigarettes is increasing rapidly that terrible. More than 109 countries
        of the world including India, Sri Lanka, and Bhutan have taken necessary initiatives to ban and control
        e-cigarettes. Now is the right time to ban it in Bangladesh too.
      </p>
      <p>
        Mr. Saifuddin Ahmed said that E-Cigarettes are a new business expansion strategy for tobacco companies. It uses
        nicotine as well as other liquids that are harmful to health. Finally, he called upon everyone to work together
        to control all products that are harmful to health.
      </p>
