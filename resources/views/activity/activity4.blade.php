    <div class="d-flex justify-content-center mb-3">
      <img src="assets/img/activity/activity4.png" alt="" style="width: 30%">
    </div>
    <div class="item">
      <p>The organisations argued that the activities of tobacco companies in
        the production and expansion of e-cigarettes in the country may
        cause a serious threat to public health. </p>
      <p>
        They raised the demand through a press conference held at Dhaka
        Reporters’ Unity (DRU) in the capital on Saturday (November 25,
        2023).
      </p>
      <p>
        The organisations who jointly arranged the press conference included
        AID Foundation, ARK Foundation, Bangladesh Anti-Tobacco Alliance
        (BATA), BNTTP, Bangladesh Cancer Society,  Dhaka Ahsania Mission,
        Development Activities of Society (DAS), Initiative for Public
        Health Research and Communication (IPHRC), Inter Press Network-IPN,
        National Heart Foundation, Tobacco Control and Research Cell and WBB
        Trust.
      </p>
      <p>
        Public health experts, researchers on tobacco control and
        anti-tobacco activists at the press conference said that recently
        the Ministry of Health and Family Welfare has proposed to ban the
        promotion, distribution, import, export and marketing of
        e-cigarettes in the amendment proposal of the tobacco control law.
        However, several multinational cigarette companies are adopting
        various methods to spread the new tobacco product in Bangladesh in
        different ways. As a result, the tobacco companies are putting the
        country’s public health at serious risk, they added.
      </p>
      <p>
        The speakers of the press conference also said that the British
        American Tobacco (BAT) has recently sent a letter to the Ministry of
        Commerce seeking permission to import some new international quality
        products citing the modern lifestyle of consumers in Bangladesh. In
        the letter, they said that they will sell all these products in the
        Bangladesh market and later expressed their intention to produce and
        export these products permanently. Such efforts are of great concern
        for the public health of the country, they added.
      </p>
      <p>
        Speaking at the press conference, president of Bangladesh Cancer
        Society Professor Dr Golam Mohiuddin Faruque said that the tobacco
        companies are attracting young people and smokers to e-cigarettes by
        using less harmful terms. E-cigarette is not less harmful, but a
        very harmful product, he added.
      </p>
      <p>
        Head of Department of Epidemiology & Research of National Heart
        Foundation Professor Dr Sohel Reza Choudhury said that the Ministry
        of Health and Family Welfare has recommended banning of e-cigarettes
        in the draft of the proposed amendment to the tobacco control law.
        More than 150 MPs have also recommended the ban on e-cigarette.
        E-cigarette promotion and spread in Bangladesh has not increased
        much so far. In such a situation, if e-cigarettes are not reined in
        now, it will not be possible to control it later, he added.
      </p>
      <p>
        Iqbal Masud of Dhaka Ahsania Mission said that cigarette companies
        are not only violating the law by using various organisations
        including the media, they are also trying to promote e-cigarettes.
      </p>
      <p>
        BAT is secretly working to promote e-cigarettes and many e-cigarette
        shops in Dhaka have been found to be operating under their auspices,
        he mentioned.
      </p>
      <p>
        The press conference was presided over by the founding president of
        MANAS Professor Dr. Arup Ratan Chowdhury. He said that despite the
        government’s goal of building a tobacco-free Bangladesh, commitment
        to achieve SDGs and constitutional responsibility to protect public
        health, cigarette companies are violating the law in this country.
      </p>
      <p>
        Project Manager of TCRC Farhana Zama Liza moderated the event, while
        Project Director of Aid Foundation Shagufta Sultana, Director of WBB
        Trust Gaous Pearee and Aminul Islam Bakul of DAS also spoke there,
        among others.
      </p>
      <p>
        Nine-point recommendation was placed at the press conference to stop
        e-cigarettes in Bangladesh. The recommendations are-
      </p>

      <p>
        (1) Necessary measures should be taken as soon as possible to stop
        the import, export, production, sale, marketing, advertising and
        promotion of e-cigarettes in Bangladesh.
      </p>
      <p>
        (2) Prohibiting the use of e-cigarettes in dramas, movies and web
        series.
      </p>
      <p>
        (3) Prohibiting direct and indirect advertising of e-cigarettes,
        including online business sites.
      </p>
      <p>
        (4) Prohibiting the import and export of e-cigarette products in the
        import policy of the Ministry of Commerce.
      </p>
      <p>
        (5) Withdrawal of HR code product list of all such products
        including e-cigarettes, its devices, e-liquids, refills by the
        Finance Division and the National Board of Revenue (NBR ).
      </p>
      <p>
        (6) Not to allow e-cigarettes, vape or any other new addictive or
        tobacco-like or nicotine-containing products in Bangladesh, as
        directed by the Supreme Court.
      </p>
      <p>
        (7) Investigating the secret activities of cigarette companies,
        institutions and individuals involved in the promotion of
        e-cigarettes and taking strict action against the companies.
      </p>
      <p>
        (8) Cancellation of any trademark or registration of e-cigarette or
        vaping products.
      </p>
      <p>
        (9) Making Bangladesh free of e-cigarettes by rapidly passing the
        proposed amendment draft of the tobacco control law.
      </p>
