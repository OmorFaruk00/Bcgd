      <p>
        Speakers at a seminar said that it has become necessary to introduce the standard packaging for tobacco products
        as well the plain packaging in order to prevent revenue evasion.
      </p>
      <p>
        Tobacco Control and Research Cell (TCRC) of Dhaka International University arranged the seminar titled
        ‘Importance of Standard Packaging Model to Protect Tobacco Tax Evasion in Bangladesh’ at Lakeshore Hotel at
        Gulshan in Dhaka on Tuesday (October 5). 
      </p>
      <p>
        Program Manager of WBB Trust Syeda Anonna Rahman moderated the event, while TCRC’s Programme Manager Farhana
        Zaman Liza presented the keynote paper in the seminar. 
      </p>
      <div class="d-flex justify-content-center mb-3">
        <img src="assets/img/activity/activity55.png" alt="" style="width: 40%;height:300px">
      </div>
      <p>
        President of NATAB Muzaffar Hossain Paltu chaired the event, while Pabna-1 MP Advocate Shamsul Haque Tuku,
        Gaibandha-1 MP Barrister Shameem Haider Patwari, Nilphamari-3 MP Rana Mohammad Sohail, Lead Consultant of
        Campaign for Tobacco Free Kids (CTFK-Bangladesh) Md Mostafizur Rahman, Country Manager of Vital Strategies Md
        Nasir Uddin Sheikh, Farida Akhter of Ubinig and Shagufta Sultana, Project Director of Aid Foundation, took part
        in the discussion on the keynote.     
      </p>
      <p>
        Representatives of Grambangla, DAS, Tabinaz and Bangladesh Anti-Tobacco Alliance (BATA) also attended the
        seminar. 
      </p>
      <p>
        In her keynote, Farhana Zaman Liza presented the data and statistics of monitoring reports that were collected
        from 265 marketplaces in 64 districts in eight phases. A total of 10,074 packs of cigarettes, bidi, jorda and
        gul were monitored. The study found differences in size, poor packaging and other faults. Such poor and
        deceptive packaging is mainly used to evade revenue and cheat the tobacco users. The introduction of standard
        packaging will be the only solution to the problem, she mentioned. In his presidential speech, Muzaffar Hossain
        Paltu said that the majority of the MPs have now started working for tobacco control. However, it is unfortunate
        that many decisions are being made at the administrative level for the spread of tobacco. It is contradictory to
        building a tobacco free Bangladesh. It is not possible to build a tobacco-free country with state partnership in
        tobacco companies, she added. 
      </p>
      <p>
        Speaking as the chief guest, Advocate Shamsul Haque Tuku said, “We have to reduce the tobacco production in
        order to reduce the demand. The tobacco companies are evading revenue in various ways. The introduction of
        standard packaging of tobacco products will play a vital role in preventing evasion, he opined. 
      </p>
      <p>
        Barrister Shameem Haider Patwari said that the investment in tobacco is increasing in Bangladesh, while the
        tobacco trade is being stopped in different countries of the world. “Tobacco products are available in our
        country. For this reason, the number of tobacco users in the country is decreasing very slowly. The introduction
        of standard packaging of tobacco products will increase revenue in the country as well as reduce the tobacco
        use,” he added. 
      </p>
      <p>
        Rana Mohammad Sohail said those involved in tobacco production are strong. In many ways, they will create
        obstacles in the implementation of the goal of building a tobacco-free Bangladesh by 2040. However, the
        government is committed to making the country tobacco free.
      </p>
